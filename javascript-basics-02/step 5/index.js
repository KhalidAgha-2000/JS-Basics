function myOver(img) {
    if (img == "image1") {
        document.querySelector("#image1").style.border = "10px solid red";
    } else if (img == "image2") {
        document.querySelector("#image2").style.border = "10px solid green";
    } else if (img == "image3") {
        document.querySelector("#image3").style.border = "10px solid yellow";
    }
    else if (img == "image4") {
        document.querySelector("#image4").style.border = "10px solid blue";
    }
    else if (img == "image5") {
        document.querySelector("#image5").style.border = "10px solid pink";
    }
}

